class Colorz:
    @staticmethod
    def red(self):
        """red color font"""
        return f"\033[31m{self}\033[0m"

    @staticmethod
    def green(self):
        """green color font"""
        return f"\033[32m{self}\033[0m"

    @staticmethod
    def cyan(self):
        """green color font"""
        return f"\033[36m{self}\033[0m"


    @staticmethod
    def yellow(self):
        """green color font"""
        return f"\033[93m{self}\033[0m"
    @staticmethod
    def orange(self):
        """green color font"""
        return f"\033[34m{self}\033[0m"
